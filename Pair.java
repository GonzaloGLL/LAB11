import java.util.ArrayList;
import java.util.List;

class Pair<K, V> {
    private K key;
    private V value;

    public Pair(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public V getValue() {
        return value;
    }

    @Override
    public String toString() {
        return "(" + key + ", " + value + ")";
    }
}

class ObjectNoExistException extends Exception {
    public ObjectNoExistException(String message) {
        super(message);
    }
}



